print('upper ', command.upper())

print('swapcase', command.swapcase())

print('isalnum', command.isalnum())

print('isalnum', command.isdigit())

print('slit', command.split())
print('splitlines', command.splitlines())
print('strip', command.strip())

print('0~10: ', command.strip()[0:10])






